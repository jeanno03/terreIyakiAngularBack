package terreIyaki.tool;

public class LongClass {
	
	private Long idLong;

	public LongClass() {
		super();
	}

	public LongClass(Long idLong) {
		super();
		this.idLong = idLong;
	}

	public Long getIdLong() {
		return idLong;
	}

	public void setIdLong(Long idLong) {
		this.idLong = idLong;
	}

	@Override
	public String toString() {
		return "LongClass [idLong=" + idLong + "]";
	}




	

}
